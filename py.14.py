#wap to demonstrate any 2 math built in function

import math

def demonstrate_math_functions():
    # Calculate the square root of a number
    number = 25
    square_root = math.sqrt(number)
    print(f"Square root of {number} is: {square_root:.2f}")

    # Calculate the power of a number
    base = 2
    exponent = 3
    power_result = math.pow(base, exponent)
    print(f"{base} raised to the power of {exponent} is: {power_result:.2f}")

# Call the function to demonstrate math functions
demonstrate_math_functions()